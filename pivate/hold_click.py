import pyautogui
import time

# ให้เวลาตัวเอง 5 วินาทีในการสลับไปหน้าต่างที่ต้องการ
print("เตรียมตัว... กำลังจะเริ่มใน 5 วินาที")
time.sleep(5)

# กดเมาส์ซ้ายค้างไว้
pyautogui.mouseDown()
print("เริ่มคลิกค้างไว้...")

# รอเป็นเวลา 30 นาที (30 * 60 วินาที)
time.sleep(30 * 60)

# ปล่อยเมาส์
pyautogui.mouseUp()
print("ครบ 30 นาที ปล่อยเมาส์เรียบร้อย")